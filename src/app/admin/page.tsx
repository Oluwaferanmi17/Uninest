"use client";

import React, { useEffect, useState } from "react";
import { useSession } from "next-auth/react";
import { useRouter } from "next/navigation";
import { PageLoader } from "@/components/ui/LoadingSpinner";

interface Stats {
  totalUsers: number;
  totalListings: number;
  totalBookings: number;
  totalConversations: number;
  studentsCount: number;
  hostsCount: number;
  adminsCount: number;
}

export default function AdminDashboard() {
  const { data: session, status } = useSession();
  const router = useRouter();
  const [stats, setStats] = useState<Stats | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    if (status === "unauthenticated") {
      router.push("/login");
    } else if (session && (session.user as any).role !== "ADMIN") {
      router.push("/");
    }
  }, [session, status, router]);

  useEffect(() => {
    async function fetchStats() {
      try {
        const response = await fetch("/api/admin/stats");
        if (response.ok) {
          const data = await response.json();
          setStats(data);
        }
      } catch (error) {
        console.error("Failed to fetch stats");
      } finally {
        setLoading(false);
      }
    }

    if (session && (session.user as any).role === "ADMIN") {
      fetchStats();
    }
  }, [session]);

  if (status === "loading" || loading) {
    return <PageLoader />;
  }

  if (!stats) {
    return (
      <div className="flex justify-center items-center h-[calc(100vh-4rem)]">
        <p className="text-red-500">Failed to load platform statistics.</p>
      </div>
    );
  }

  return (
    <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-8">
      <div className="mb-8">
        <h1 className="text-3xl font-bold text-gray-900">Admin Dashboard</h1>
        <p className="text-gray-500 mt-2">Platform overview and statistics</p>
      </div>

      <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-4 gap-6">
        <StatCard title="Total Users" value={stats.totalUsers} icon="users" />
        <StatCard title="Students" value={stats.studentsCount} icon="academic-cap" />
        <StatCard title="Hosts" value={stats.hostsCount} icon="home" />
        <StatCard title="Admins" value={stats.adminsCount} icon="shield-check" />
        <StatCard title="Total Listings" value={stats.totalListings} icon="office-building" />
        <StatCard title="Total Bookings" value={stats.totalBookings} icon="calendar" />
        <StatCard title="Total Conversations" value={stats.totalConversations} icon="chat" />
      </div>
    </div>
  );
}

